    private void btnCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariActionPerformed
        // TODO add your handling code here:
        try {
            load_data1(); 
            String sql = "SELECT * FROM event WHERE id = ?";
            java.sql.Connection conn = (Connection) Koneksi.getKoneksi();
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, eventCari.getText());
            java.sql.ResultSet res = pst.executeQuery();

            if (res.next()) {
                eventID.setText(res.getString("id"));
                eventNama.setText(res.getString("nama"));
                eventTempat.setText(res.getString("tempat"));
                eventJenis.setText(res.getString("jenis"));
                eventKeterangan.setText(res.getString("ket"));
            } else {
                JOptionPane.showMessageDialog(null, "Data tidak ditemukan");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnCariActionPerformed

    private void load_data1() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("No");
        model.addColumn("Nama");
        model.addColumn("Tempat");
        model.addColumn("Jenis");
        model.addColumn("Keterangan");

        try {
            String sql = "SELECT * FROM event";
            java.sql.Connection conn = (Connection) Koneksi.getKoneksi();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            int no = 1;
            while (res.next()) {
                model.addRow(new Object[]{
//                    no++,
                    res.getString("id"),
                    res.getString("nama"),
                    res.getString("tempat"),
                    res.getString("jenis"),
                    res.getString("ket")
                });
            }
            tabelCRUD.setModel(model);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
    
