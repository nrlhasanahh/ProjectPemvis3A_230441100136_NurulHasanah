    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        try {
            if (eventID.getText().isEmpty() ||eventNama.getText().isEmpty() || eventTempat.getText().isEmpty() || eventJenis.getText().isEmpty() || eventKeterangan.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Harap lengkapi semua data!");
                return; 
            }
            
            String sql = "INSERT INTO event (id, nama, tempat, jenis, ket) VALUES (?, ?, ?, ?, ?)";
            java.sql.Connection conn = (Connection) Koneksi.getKoneksi();
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, eventID.getText());
            pst.setString(2, eventNama.getText());
            pst.setString(3, eventTempat.getText());
            pst.setString(4, eventJenis.getText());
            pst.setString(5, eventKeterangan.getText());
            pst.execute();

            JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
            load_data1();
            kosong1();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }  
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        try {
            int confirm = JOptionPane.showConfirmDialog(null, "Apakah Anda yakin ingin menyimpan perubahan data ini?", "Konfirmasi Perubahan", JOptionPane.YES_NO_OPTION);

            if (confirm == JOptionPane.YES_OPTION) {
                String sql = "UPDATE event SET nama = ?, tempat = ?, jenis = ?, ket = ? WHERE id = ?";
                java.sql.Connection conn = (Connection) Koneksi.getKoneksi();
                java.sql.PreparedStatement pst = conn.prepareStatement(sql);
                pst.setString(1, eventNama.getText());
                pst.setString(2, eventTempat.getText());
                pst.setString(3, eventJenis.getText());
                pst.setString(4, eventKeterangan.getText());
                pst.setString(5, eventID.getText());

                int rowsUpdated = pst.executeUpdate();
                if (rowsUpdated > 0) {
                    JOptionPane.showMessageDialog(null, "Data Berhasil Diupdate");
                } else {
                    JOptionPane.showMessageDialog(null, "Data Gagal Diupdate");
                }

                load_data1(); 
                kosong1();     
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        try {
            String id = eventID.getText();

            if (id.isEmpty()) {
                JOptionPane.showMessageDialog(null, "ID belum dipilih. Silakan pilih data terlebih dahulu.");
            } else {
                int confirm = JOptionPane.showConfirmDialog(null, "Apakah Anda yakin ingin menghapus data ini?", "Konfirmasi Penghapusan", JOptionPane.YES_NO_OPTION);

                if (confirm == JOptionPane.YES_OPTION) {
                    String sql = "DELETE FROM event WHERE id = ?";
                    java.sql.Connection conn = (Connection) Koneksi.getKoneksi();
                    java.sql.PreparedStatement pst = conn.prepareStatement(sql);
                    pst.setString(1, id);
                    pst.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Data Berhasil Dihapus");
                    load_data1();
                    kosong1();
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnHapusActionPerformed

