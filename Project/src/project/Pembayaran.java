    private void btnBayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBayarActionPerformed
        // TODO add your handling code here: 
        try {
            if (TFnamaData1.getText().isEmpty() || TFalamat1.getText().isEmpty() || TFtlp1.getText().isEmpty() 
                || TFtotal.getText().isEmpty() || TFcash.getText().isEmpty() || CBmetodeBayar.getSelectedItem() == null 
                || CBmetodeBayar.getSelectedIndex() == 0) {
                JOptionPane.showMessageDialog(null, "Harap lengkapi semua data dan pilih metode pembayaran yang valid!");
                return; 
            }

            String totalInput = TFtotal.getText().replaceAll("[^0-9]", ""); 
            String cashInput = TFcash.getText().replaceAll("[^0-9]", "");   

            int total = Integer.parseInt(totalInput); 
            int cash = Integer.parseInt(cashInput);   

            if (cash < total) {
                JOptionPane.showMessageDialog(null, "Uang anda tidak mencukupi!");
                return;
            } else if (cash > total) {
                JOptionPane.showMessageDialog(null, "Masukkan cash sesuai total belanja!");
                return;
            }

            int confirm = JOptionPane.showConfirmDialog(null, "Apakah anda yakin ingin membeli?", "Konfirmasi Pembayaran", JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {
                String metodePembayaran = CBmetodeBayar.getSelectedItem().toString();
                String sql = "INSERT INTO riwayat1 (nama, alamat, telepon, total, cash, metode) VALUES (?, ?, ?, ?, ?, ?)";
                java.sql.Connection conn = (Connection) Koneksi.getKoneksi();
                java.sql.PreparedStatement pst = conn.prepareStatement(sql);
                pst.setString(1, TFnamaData1.getText());
                pst.setString(2, TFalamat1.getText());
                pst.setString(3, TFtlp1.getText());
                pst.setInt(4, total); 
                pst.setInt(5, cash); 
                pst.setString(6, metodePembayaran);
                pst.execute();

                JOptionPane.showMessageDialog(null, "Pembayaran berhasil");
                load_data(); 
                kosong();    
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage());
        }
        this.dispose();
    }//GEN-LAST:event_btnBayarActionPerformed

