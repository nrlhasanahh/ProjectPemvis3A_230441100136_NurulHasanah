    private void load_data() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("No"); 
        model.addColumn("Nama");
        model.addColumn("Alamat");
        model.addColumn("Telepon");
        model.addColumn("Total");
        model.addColumn("Cash");
        model.addColumn("Metode Pembayaran");

        try {
            String sql = "SELECT * FROM riwayat1";
            java.sql.Connection conn = (Connection) Koneksi.getKoneksi();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);

            int no = 1; 
            while (res.next()) {
                model.addRow(new Object[]{
                    no++, 
                    res.getString("nama"),
                    res.getString("alamat"),
                    res.getString("telepon"),
                    res.getString("total"),
                    res.getString("cash"),
                    res.getString("metode")
                });
            }

            Desain frameDesain = new Desain();
            frameDesain.getTabbedPane().setSelectedIndex(2); 
            frameDesain.getTabelPelanggan().setModel(model); 
            frameDesain.setVisible(true); 
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error memuat data: " + e.getMessage());
        }
    }




    
